/**
 * 从 data/命题三美甲评测数据（对外版）.xlsx 生成 mock/eval-hands.js
 * 用法: node scripts/import-eval-hands.js
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const dataDir = path.join(__dirname, '../data');
const outFile = path.join(__dirname, '../mock/eval-hands.js');
const py = `
import openpyxl, os, json
data_dir = ${JSON.stringify(dataDir.replace(/\\/g, '/'))}
xlsx = [f for f in os.listdir(data_dir) if f.endswith('.xlsx')][0]
wb = openpyxl.load_workbook(os.path.join(data_dir, xlsx), read_only=True, data_only=True)
ws = wb[wb.sheetnames[0]]
rows = []
for i, row in enumerate(ws.iter_rows(min_row=2, values_only=True)):
    if row and row[0]:
        rows.append({'id': 'eval-' + str(i+1), 'label': '评测手照' + str(i+1), 'handUrl': row[0], 'pairStyleUrl': row[1] or ''})
print(json.dumps(rows, ensure_ascii=False))
`;

const rows = JSON.parse(execSync(`python -c "${py.replace(/"/g, '\\"').replace(/\n/g, ';')}"`, { encoding: 'utf8' }));
const content = [
  '// AUTO-GENERATED by scripts/import-eval-hands.js — DO NOT EDIT BY HAND',
  'module.exports = ' + JSON.stringify(rows, null, 2) + ';',
  ''
].join('\n');
fs.writeFileSync(outFile, content, 'utf8');
console.log('Wrote', rows.length, 'hands ->', outFile);
